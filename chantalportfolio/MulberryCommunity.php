				<p>					
The Mulberry community was a group project designed for an aging population of individuals with intellectual developmental disabilities (IDD). These intellectual differences include down syndrome, autism and dementia. As generations today are living longer, there is a growing population of individuals with IDD. This poses a concern because there are limited options for support, especially for those who are ages 21 and over. Our goal was to design a community that celebrates their neurological differences by providing them with living quarters that encourage independent living, social interaction, meaningful experiences, and a better quality of life.
                </p>
                <p>
The life size chess and checker boards was an individual development. It was designed to include an element of fun while encouraging social interaction and problem solving.
Each game piece was designed with wheels for easy maneuver. The chess pieces display instructions on a small screen to indicate the moves allowed by each piece. The overall design is simple but accommodates the needs of those with intellectual diversities. 

